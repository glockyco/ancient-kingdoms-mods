#!/usr/bin/env node
/**
 * Build-time precomputation of the entity counts shown on the home page.
 *
 * The home page is server-rendered on Cloudflare Workers so it can inline the
 * live game version into its initial HTML. Workers cannot use better-sqlite3,
 * so any data the home page needs at request time must be available without
 * touching the SQLite database.
 *
 * These counts only change when a new compendium snapshot is built, so we
 * snapshot them at prebuild time and import the generated module from the
 * home page's load function. Staleness characteristics are identical to the
 * previous prerender setup (counts were baked into HTML at build time then,
 * are baked into a TS module now).
 *
 * Run automatically via the "prebuild" npm script. Output is gitignored.
 */

import Database from "better-sqlite3";
import { writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");
const dbPath = resolve(projectRoot, "static/compendium.db");
const outDir = resolve(projectRoot, "src/lib/generated");
const outPath = resolve(outDir, "home-counts.ts");

const db = new Database(dbPath, { readonly: true });
const count = (sql) => /** @type {{ count: number }} */ (db.prepare(sql).get()).count;

const items = count("SELECT COUNT(*) as count FROM items");
const monsters = count("SELECT COUNT(*) as count FROM monsters WHERE is_dummy = 0");
const npcs = count("SELECT COUNT(*) as count FROM npcs");
const classes = count("SELECT COUNT(*) as count FROM classes");
const skills = count("SELECT COUNT(*) as count FROM skills");
const pets = count("SELECT COUNT(*) as count FROM pets");
const zones = count("SELECT COUNT(*) as count FROM zones");
const quests = count("SELECT COUNT(*) as count FROM quests");
const altars = count("SELECT COUNT(*) as count FROM altars");
const professions = count("SELECT COUNT(*) as count FROM professions");
const gatheringResources = count("SELECT COUNT(*) as count FROM gathering_resources");
const chests = count("SELECT COUNT(*) as count FROM chests");
const alchemyRecipes = count("SELECT COUNT(*) as count FROM alchemy_recipes");
const craftingRecipes = count("SELECT COUNT(*) as count FROM crafting_recipes");
const scribingRecipes = count("SELECT COUNT(*) as count FROM scribing_recipes");

db.close();

const counts = {
  items,
  monsters,
  npcs,
  classes,
  skills,
  pets,
  zones,
  quests,
  altars,
  professions,
  gatheringResources,
  recipes: alchemyRecipes + craftingRecipes + scribingRecipes,
  chests,
};

const out = `// AUTO-GENERATED by scripts/generate-home-counts.mjs — do not edit.
// Snapshot of entity counts taken at prebuild time. The home page is
// server-rendered on Cloudflare Workers and must not touch SQLite at request
// time, so these are baked into the bundle.

export interface HomeCounts {
  readonly items: number;
  readonly monsters: number;
  readonly npcs: number;
  readonly classes: number;
  readonly skills: number;
  readonly pets: number;
  readonly zones: number;
  readonly quests: number;
  readonly altars: number;
  readonly professions: number;
  readonly gatheringResources: number;
  readonly recipes: number;
  readonly chests: number;
}

export const HOME_COUNTS: HomeCounts = ${JSON.stringify(counts, null, 2)};
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outPath, out);

console.log(`Wrote ${outPath}`);
